#Crea un arreglo con 4 números de tu elección. Luego, escribe un código que sume estos números y muestre el resultado en la pantalla.

# Crear un arreglo con números
numeros = [7, 10, 60, 8, 3]

# Inicializar la variable suma
suma = 0

# Recorrer el arreglo y sumar cada número
for numero in numeros:
 suma += numero

# Mostrar el resultado de la suma
print("La suma de los números es:", suma)
